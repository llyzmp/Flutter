void main(List<String> arguments) {
// Numbers: int
  int score = 22;
  var count = 33;
  int hexValue = 0xEADEBAEE;

  print(score);
  print(count);
  print(hexValue);

  // Numbers: double
  double percentage = 89.4;
  var precent = 9.887;
  double exponents = 1.23e5;

  print(percentage);
  print(precent);
  print(exponents);

  //String
  String name = '熊大';
  var company = 'alibaba';

  print(name);
  print(company);

  // Boolean
  bool isValid = true;
  var isAlive = false;

  print(isValid);
  print(isAlive);
}
